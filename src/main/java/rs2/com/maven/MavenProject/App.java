package rs2.com.maven.MavenProject;

/**
 * Simple App for a Maven project that does arithmetic.
 *
 */
public class App 
{
	public App()
	{}
	
	/**
	 * This method is used to sum 2 numbers.
	 * @param x The 1st number to sum.
	 * @param y The 2nd number to sum.
	 * @return The summed up number of x and y.
	 */
    public int summation(int x, int y)
    {
    	return x + y;
    }
    
}
